// ***************** newLaser ****************
// manage the laser Array
// Inputs:  none
// Outputs: none
void newLaser(uint32_t buttons){
		if((buttons & 0x01) > 0 && button0Wait == 0){	//if the right button has been pressed
			PF1 ^= 0x02;		//toggle the led
			LaserTrack[laserPt].Active = 1;		//show that a new laser is ready to be shown
			LaserTrack[laserPt].X = Convert(ADCMail) - 3;	//position the laser on the right
			LaserTrack[laserPt].Y = 150;
			button0Wait = 10;
			if(laserPt < 12)
				laserPt++;
			else
				laserPt = 0;
		}
		if((buttons & 0x02) > 0 && button1Wait == 0){	//if the left button is pressed
			PF2 ^= 0x04;	//toggle the led
			LaserTrack[laserPt].Active = 1;	//show that a laser is ready to be displayed
			LaserTrack[laserPt].X = Convert(ADCMail) + 15;	//position the laser to the left of the ship
			LaserTrack[laserPt].Y = 150;
			button1Wait = 10;
			if(laserPt < 12)
				laserPt++;
			else
				laserPt = 0;
		}
		laserFlag = 1;
}

// ***************** PortD_Handler ****************
// Activate Port D for interrupts
// Inputs:  none
// Outputs: none
void GPIOPortD_Handler(){
	GPIO_PORTD_ICR_R = 0x3; //clear flags
	PF3 ^= 0x08;		//toggle the led to show it is working
	if(button0Wait == 0 || button1Wait == 0){	//if a button is pressed, interrupt and display a new laser
		setshoot();
		newLaser(GPIO_PORTD_DATA_R & 0x03);
	}
}

// ***************** SysTick_Init ****************
// Activate systick interrupts to run user task periodically
// Inputs:  task is a pointer to a user function
//          period in units (1/clockfreq)
// Outputs: none
void SysTick_Init(void){
	NVIC_ST_CTRL_R = 0;
	NVIC_ST_RELOAD_R = 3000000;
	NVIC_ST_CURRENT_R = 0;
	NVIC_ST_CTRL_R |= 0x07;
}

// ***************** SysTick_Handler ****************
// checks ADC, increments sprite positions, and decrements button wait values
// Inputs:  none
// Outputs: none
void SysTick_Handler(void){
	uint32_t val = GPIO_PORTD_DATA_R & 0x04;
	if(val==0x04){
	pauseFlag =1;		//pause the game if the bottom button is pressed
	}
	while(pauseFlag==1){
	val = GPIO_PORTD_DATA_R & 0x04;
	if((GPIO_PORTD_DATA_R&0x08)== 0x08){
	pauseFlag=0;		//wait for the top button to be pressed in order to unpause the game
	}
	}
	ADCMail = ADC_In();	// determine location of the ship
	if((ADCMail/100 == ADCLast/100))		//if ship has not moved dont trigger it to be drawn again
		shipFlag = 0;
	else
	{
		shipFlag = 1;
		ADCLast = ADCMail;		//get new ship location
	}
	for(int i = 0; i < 12; i++)
	{
		if(LaserTrack[i].Active == 1){
			LaserTrack[i].Y -= 4;		//move the laser. this is the speed of the laser. to increase make higher
			laserFlag = 1;
		}
		if(AstroidTrack[i].Active == 1){
			AstroidTrack[i].Y += 2;		//this is the speed of the astroid. to increase it make it higher
			AstroidFlag = 1;
		}
	}
	if(button0Wait != 0 && button0Wait > 0)
		button0Wait--;
	if(button1Wait != 0 && button1Wait > 0)
		button1Wait--;
}


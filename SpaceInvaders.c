// ***************** hitCheck ****************
// run by GameEngine to check hitboxes 
// Inputs: none
// Outputs: none
void hitCheck(){
	for(int i =0; i < 12; i++){
		for(int j = 0; j < 6; j++){
			if((LaserTrack[i].X >= AstroidTrack[j].X - 2) && (LaserTrack[i].X <= AstroidTrack[j].X + 12) && 
				(LaserTrack[i].Y + 9 >= AstroidTrack[j].Y) && (LaserTrack[i].Y <= AstroidTrack[j].Y + 11) && (AstroidTrack[j].Active == 1)){
				eraseSprites(i,j);
				}
		
			if((AstroidTrack[j].Y >= 138) && ((Convert(ADCMail) < AstroidTrack[j].X + 15) && (Convert(ADCMail) + 21 > AstroidTrack[j].X))  
				&& (AstroidTrack[j].Active == 1))		//if a collsions occurs end the game
					endScreen();
			}
		}
	for(int j=0; j<6; j++){
	if(AstroidTrack[j].Y > 175){
		if(AstroidTrack[j].Active == 1){
			AstroidTrack[j].Active = 0;	//if an astroid gets through in a lane, close it and
			shipLives=shipLives-1;			//decrease the number of lives by one
			drawheart();
		}
	}
}
	if(shipLives<=0){
	ST7735_DrawBitmap(0, 10, &ExplosionClear[0], 13, 10);	//erases heart
	gameOver();
	}
		if(AstroidTrack[0].Active == 0 && AstroidTrack[1].Active == 0 && AstroidTrack[2].Active == 0 && AstroidTrack[3].Active == 0 && AstroidTrack[4].Active == 0
			&& AstroidTrack[5].Active == 0)
			gameOver();			//if one astroid has gotten past in all of the 6 lanes end the game
	}

// ***************** hitCheck ****************
// run by GameEngine to check hitboxes 
// Inputs: none
// Outputs: none
void explosionCycle(){
	for(int i = 0; i < 8; i++){
		if(ExplosionTrack[i].State != 0){
			if(ExplosionTrack[i].State == 1)
				ST7735_DrawBitmap(ExplosionTrack[i].X, ExplosionTrack[i].Y, &ExplosionA[0], 12, 13);	//beginning of explosion
			else if(ExplosionTrack[i].State == 2)
				ST7735_DrawBitmap(ExplosionTrack[i].X, ExplosionTrack[i].Y, &ExplosionB[0], 16, 16);
			else if(ExplosionTrack[i].State == 3)
				ST7735_DrawBitmap(ExplosionTrack[i].X, ExplosionTrack[i].Y, &ExplosionC[0], 30, 28);	//main explosion
			else if(ExplosionTrack[i].State == 4)
				ST7735_DrawBitmap(ExplosionTrack[i].X, ExplosionTrack[i].Y, &ExplosionClear[0], 30, 28);		//clear the explosion
			if(ExplosionTrack[i].State == 4)
				ExplosionTrack[i].State = 0;
			else{
				ExplosionTrack[i].State++;
				explosionDelay = 25;
			}
		}
	}
}



// ***************** outNumber ****************
// used by outscore to output a number bitmap to a certain location   
// Inputs: value to be outputted, x coordinate, y coordinate
// Outputs: none
void outNumber(uint16_t value, uint16_t X, uint16_t Y){
	if(value == 0)
		ST7735_DrawBitmap(X, Y, &zero[0], 7, 9);
	else if(value == 1)
		ST7735_DrawBitmap(X, Y, &one[0], 7, 9);
	else if(value == 2)
		ST7735_DrawBitmap(X, Y, &two[0], 7, 9);
	else if(value == 3)
		ST7735_DrawBitmap(X, Y, &three[0], 7, 9);
	else if(value == 4)
		ST7735_DrawBitmap(X, Y, &four[0], 7, 9);		//each is a different image of a number to outupt for the score
	else if(value == 5)
		ST7735_DrawBitmap(X, Y, &five[0], 7, 9);
	else if(value == 6)
		ST7735_DrawBitmap(X, Y, &six[0], 7, 9);
	else if(value == 7)
		ST7735_DrawBitmap(X, Y, &seven[0], 7, 9);
	else if(value == 8)
		ST7735_DrawBitmap(X, Y, &eight[0], 7, 9);
	else
		ST7735_DrawBitmap(X, Y, &nine[0], 7, 9);
}

// ***************** outScore ****************
// run to output Score   
// Inputs: none
// Outputs: none
void outScore(){
		for(int i = 0; i < 4; i++){
			tempScore = (score - temp);
			tempScore /= scoreVariable;
			outNumber(tempScore, XScore, YScore);	//split up the score into 3 different numbers
			XScore += 8;
			temp += (tempScore * scoreVariable);
			scoreVariable /= 10;	
		}
		XScore = 96;
		scoreVariable = 1000;
		temp = 0;
	}

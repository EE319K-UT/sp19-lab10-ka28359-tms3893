// Activate Port D
// Inputs:  none
// Outputs: none
void PortD_Init(void){
  GPIO_PORTD_AMSEL_R = 0x00;    
  GPIO_PORTD_AFSEL_R = 0x00;        
	GPIO_PORTD_DIR_R &= !0x0F;
	GPIO_PORTD_DEN_R |= 0x0F;	//allow all 4 buttons to be used on port d
	startScreen();
	GPIO_PORTD_IS_R &= !0x03;	//PD is edge-sensitive
	GPIO_PORTD_IBE_R &= !0x03;	//PD is not both edges
	GPIO_PORTD_IEV_R = 0x03;		//PD falling edge event
	GPIO_PORTD_ICR_R = 0x01;		// clear flag
	GPIO_PORTD_IM_R |= 0x03;		// enable interrupt on PD
	NVIC_PRI0_R = (NVIC_PRI0_R & 0x00FFFFF) | 0xA0000000;
	NVIC_EN0_R = 0x08;	
}

